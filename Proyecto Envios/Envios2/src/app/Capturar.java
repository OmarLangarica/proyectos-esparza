    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        try{
            int fila = tblClientes.getSelectedRow();
            int id = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
            
            PreparedStatement ps;
            ResultSet rs;
            
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT cliid,cliNombre, cliApellidos, cliSexo, CliLimiteCredito, tipid FROM CLIENTES WHERE cliid=?");
            ps.setInt(1, id);
            rs=ps.executeQuery();
            
            while(rs.next()){
                txtID.setText(String.valueOf(id));
                txtNombre.setText(rs.getString("cliNombre"));
                txtApellidos.setText(rs.getString("cliApellidos"));
                if(rs.getString("cliSexo").equals("M")){
                    rbMasculino.setSelected(true);
                }else if(rs.getString("cliSexo").equals("F")){
                    rbFemenino.setSelected(true);
                }       
                txtLimiteCredito.setText(rs.getString("cliLimiteCredito")); 
                int tipoId = rs.getInt("tipid"); // Obtener el número de la base de datos

                // Asegurar que el valor esté en el rango correcto
                if (tipoId >= 1 && tipoId <= 4) {
                 cbTipo.setSelectedIndex(tipoId); // Asignar el índice correspondiente
                } else {
                    cbTipo.setSelectedIndex(0); // Si el valor es inválido, seleccionar "Seleccione"
                }

            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error al asignar los valores "+e.getMessage());

        }
    }//GEN-LAST:event_tblClientesMouseClicked

