    private void eliminarRegistro() {
        String idTexto = txtID.getText().trim();

        if (idTexto.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingrese un ID para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }

        int id;
        try {
         id = Integer.parseInt(idTexto);
            if (id <= 0) {
            JOptionPane.showMessageDialog(null, "El ID debe ser un número positivo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un número válido para el ID", "Error", JOptionPane.ERROR_MESSAGE);
            return;
         }
        
        try{
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("DELETE FROM CLIENTES WHERE cliid=?");
            ps.setInt(1,id);
            if(ps.executeUpdate()>0){
                JOptionPane.showMessageDialog(null,"Registro eliminado exitosamente");
                limpiar();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null,"El registro no existe");

            }
            
   
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error al eliminar"+e.getMessage());
        }
    }

    private void agregarRegistro() {
        if (!validarCampos()) {
        return; 
        }
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        String sexo="";
        if(rbMasculino.isSelected()==true){
            sexo = "M";
        }else if(rbFemenino.isSelected()==true){
            sexo = "F";
        }
        String LimiteCredito = txtLimiteCredito.getText();
        String tipo = (String)cbTipo.getSelectedItem();
        
        try{
            Connection con = Conexion.getConexion();
            CallableStatement cs = con.prepareCall("{CALL sp_grabar(?,?, ?, ?, ?, ?)}");
             // Registrar el parámetro de salida (clave primaria)
            cs.registerOutParameter(1, java.sql.Types.INTEGER);

            cs.setNull(1, java.sql.Types.INTEGER); 
            cs.setString(2, nombre);
            cs.setString(3, apellidos);
            cs.setString(4, sexo);
            cs.setDouble(5, Double.parseDouble(LimiteCredito)); 
            int opcion = cbTipo.getSelectedIndex();
            cs.setInt(6, opcion); 
            cs.execute();
            JOptionPane.showMessageDialog(null,"Registro agregado exitosamente");
            limpiar();
            cargarTabla();
   
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error al insertar "+e.getMessage());
        }
    }
    
    private boolean validarCampos() {
    String nombre = txtNombre.getText().trim();
    String apellidos = txtApellidos.getText().trim();
    String sexo = (rbMasculino.isSelected() || rbFemenino.isSelected()) ? "M" : "";
    String limiteCredito = txtLimiteCredito.getText().trim();
    int tipoIndex = cbTipo.getSelectedIndex();

    // Expresión regular para validar solo letras (permite espacios y acentos)
    String regexLetras = "^[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+$";

    if (!nombre.matches(regexLetras)) {
        JOptionPane.showMessageDialog(null, "El nombre solo debe contener letras", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (!apellidos.matches(regexLetras)) {
        JOptionPane.showMessageDialog(null, "Los apellidos solo deben contener letras", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (nombre.isEmpty() || apellidos.isEmpty() || sexo.isEmpty() || limiteCredito.isEmpty() || tipoIndex == -1) {
        JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    try {
        double limite = Double.parseDouble(limiteCredito);
        if (limite < 0) {
            JOptionPane.showMessageDialog(null, "El límite de crédito no puede ser negativo", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Ingrese un número válido en el límite de crédito", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true; 
}


    private void modificarRegistro() {
    String idTexto = txtID.getText().trim();
    String nombre = txtNombre.getText().trim();
    String apellidos = txtApellidos.getText().trim();
    String sexo = rbMasculino.isSelected() ? "M" : (rbFemenino.isSelected() ? "F" : "");
    String limiteCreditoTexto = txtLimiteCredito.getText().trim();
    int tipo = cbTipo.getSelectedIndex();

    // Expresiones regulares para validación
    String regexLetras = "^[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+$"; // Solo letras y espacios
    String regexNumeros = "^[0-9]+$"; // Solo números positivos

    // Validar ID
    if (idTexto.isEmpty() || !idTexto.matches(regexNumeros) || Integer.parseInt(idTexto) <= 0) {
        JOptionPane.showMessageDialog(null, "Ingrese un ID válido para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int id = Integer.parseInt(idTexto); // Convertir a entero después de validar

    if (!nombre.matches(regexLetras)) {
        JOptionPane.showMessageDialog(null, "El nombre solo debe contener letras y espacios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!apellidos.matches(regexLetras)) {
        JOptionPane.showMessageDialog(null, "Los apellidos solo deben contener letras y espacios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (sexo.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Seleccione el sexo.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    double limiteCredito;
    try {
        limiteCredito = Double.parseDouble(limiteCreditoTexto);
        if (limiteCredito < 0) {
            JOptionPane.showMessageDialog(null, "El límite de crédito no puede ser negativo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Ingrese un número válido en el límite de crédito.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // intenta actualizar el registro en la base de datos
    try {
        Connection con = Conexion.getConexion();
        CallableStatement cs = con.prepareCall("{CALL sp_grabar(?, ?, ?, ?, ?, ?)}");

        cs.setInt(1, id); //se usa el ID existente para actualizar
        cs.setString(2, nombre);
        cs.setString(3, apellidos);
        cs.setString(4, sexo);
        cs.setDouble(5, limiteCredito);
        cs.setInt(6, tipo);

        int rowsAffected = cs.executeUpdate(); //veo cuantas filas fueron afectadas

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Registro actualizado exitosamente.");
            limpiar();
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró un registro con ID " + id, "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al actualizar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


    private void limpiar(){
        txtID.setText("");
        txtNombre.setText("");
        txtApellidos.setText("");
        groupGeneros.clearSelection();
        txtLimiteCredito.setText("");
        cbTipo.setSelectedIndex(0);
       
    }
    
    private void cargarTabla() {
    DefaultTableModel modeloTabla = (DefaultTableModel) tblClientes.getModel();
    modeloTabla.setRowCount(0);

    PreparedStatement ps;
    ResultSet rs;
    ResultSetMetaData rsmd;
    int columnas;

    try {
        Connection con = Conexion.getConexion();
        //use el inner join para que salga el nombre del tipo en la tabla
        ps = con.prepareStatement("SELECT cliid, cliNombre, cliApellidos, cliSexo, cliLimiteCredito, t.tipNombre " +
                                  "FROM CLIENTES c " +
                                  "INNER JOIN Tipos t ON c.tipid = t.tipid");
        rs = ps.executeQuery();
        rsmd = rs.getMetaData();
        columnas = rsmd.getColumnCount();

        while (rs.next()) {
            Object[] fila = new Object[columnas];
            for (int i = 0; i < columnas; i++) {
                fila[i] = rs.getObject(i + 1);
            }
            modeloTabla.addRow(fila);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar la tabla: " + e.getMessage());
    }
}

    
